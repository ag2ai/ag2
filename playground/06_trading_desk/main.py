@tool
async def check_trade_rules(trade_details: str) -> str:
    """Check a trade against regulatory rules.

    Args:
        trade_details: Description of the trade to check.
    """
    rules = [
        ("Reg T margin requirements", "PASS"),
        ("Pattern day trader check", random.choice(["PASS", "PASS", "FLAG"])),
        ("Insider trading screen", "PASS"),
        ("Short sale restrictions", random.choice(["PASS", "PASS", "REVIEW"])),
        ("Position concentration limits", "PASS"),
    ]
    lines = ["Regulatory Compliance Check:"]
    for rule, status in rules:
        marker = "OK" if status == "PASS" else status
        lines.append(f"  [{marker}] {rule}")
    flags = sum(1 for _, s in rules if s != "PASS")
    lines.append(f"  Overall: {'APPROVED' if flags == 0 else f'{flags} FLAG(S) - REVIEW REQUIRED'}")
    return "\n".join(lines)


