@tool
async def place_order(symbol: str, side: str, quantity: int, order_type: str = "market") -> str:
    """Place a trade order.

    Args:
        symbol: Stock ticker symbol.
        side: "buy" or "sell".
        quantity: Number of shares.
        order_type: "market" or "limit".
    """
    order_id = f"ORD-{random.randint(10000, 99999)}"
    base_prices = {"NVDA": 892.50, "AAPL": 178.25, "TSLA": 245.80, "GOOGL": 155.40, "MSFT": 415.60}
    base = base_prices.get(symbol, random.uniform(50, 500))
    slippage = random.uniform(-0.5, 0.5) if order_type == "market" else 0
    fill_price = round(base + slippage, 2)
    total = round(fill_price * quantity, 2)
    return (
        f"Order Executed:\n"
        f"  Order ID: {order_id}\n"
        f"  Symbol: {symbol}\n"
        f"  Side: {side.upper()}\n"
        f"  Quantity: {quantity:,}\n"
        f"  Type: {order_type.upper()}\n"
        f"  Fill Price: ${fill_price:,.2f}\n"
        f"  Total Value: ${total:,.2f}\n"
        f"  Status: FILLED"
    )


@tool
async def check_position(symbol: str) -> str:
    """Check current position for a symbol.

    Args:
        symbol: Stock ticker symbol.
    """
    qty = random.randint(0, 5000)
    avg_cost = round(random.uniform(100, 800), 2)
    current = round(avg_cost * (1 + random.uniform(-0.15, 0.15)), 2)
    pnl = round((current - avg_cost) * qty, 2)
    return (
        f"Position for {symbol}:\n"
        f"  Shares: {qty:,}\n"
        f"  Avg Cost: ${avg_cost:,.2f}\n"
        f"  Current Price: ${current:,.2f}\n"
        f"  Unrealized P&L: ${pnl:+,.2f}"
    )


@tool
async def calculate_pnl(symbol: str) -> str:
    """Calculate detailed P&L for a symbol.

    Args:
        symbol: Stock ticker symbol.
    """
    realized = round(random.uniform(-50000, 100000), 2)
    unrealized = round(random.uniform(-30000, 80000), 2)
    fees = round(random.uniform(50, 500), 2)
    net = round(realized + unrealized - fees, 2)
    return (
        f"P&L Report for {symbol}:\n"
        f"  Realized P&L: ${realized:+,.2f}\n"
        f"  Unrealized P&L: ${unrealized:+,.2f}\n"
        f"  Fees/Commission: ${fees:,.2f}\n"
        f"  Net P&L: ${net:+,.2f}"
    )


