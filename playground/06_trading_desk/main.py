@tool
async def fetch_market_data(symbol: str) -> str:
    """Fetch current market data for a symbol.

    Args:
        symbol: Stock ticker symbol (e.g. NVDA, AAPL, TSLA).
    """
    base_prices = {"NVDA": 892.50, "AAPL": 178.25, "TSLA": 245.80, "GOOGL": 155.40, "MSFT": 415.60}
    base = base_prices.get(symbol, random.uniform(50, 500))
    price = round(base * (1 + random.uniform(-0.05, 0.05)), 2)
    volume = random.randint(5_000_000, 80_000_000)
    high_52 = round(price * random.uniform(1.1, 1.5), 2)
    low_52 = round(price * random.uniform(0.5, 0.9), 2)
    return (
        f"Market Data for {symbol}:\n"
        f"  Price: ${price:,.2f}\n"
        f"  Volume: {volume:,}\n"
        f"  52-Week High: ${high_52:,.2f}\n"
        f"  52-Week Low: ${low_52:,.2f}\n"
        f"  Market Cap: ${price * random.randint(1_000, 15_000)}M"
    )


@tool
async def run_technical_analysis(symbol: str) -> str:
    """Run technical analysis on a symbol.

    Args:
        symbol: Stock ticker symbol.
    """
    rsi = round(random.uniform(20, 80), 1)
    macd = round(random.uniform(-5, 5), 2)
    sma_20 = round(random.uniform(150, 900), 2)
    sma_50 = round(random.uniform(140, 880), 2)
    if rsi > 65 and macd > 0:
        signal = "STRONG BUY"
    elif rsi > 50 and macd > 0:
        signal = "BUY"
    elif rsi < 35 and macd < 0:
        signal = "STRONG SELL"
    elif rsi < 50 and macd < 0:
        signal = "SELL"
    else:
        signal = "HOLD"
    return (
        f"Technical Analysis for {symbol}:\n"
        f"  RSI(14): {rsi}\n"
        f"  MACD: {macd}\n"
        f"  SMA(20): ${sma_20:,.2f}\n"
        f"  SMA(50): ${sma_50:,.2f}\n"
        f"  Signal: {signal}"
    )


