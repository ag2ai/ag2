async def main() -> None:
    # -- Parse CLI args -----------------------------------------------
    model = "gemini-3.1-pro-preview"
    if "--model" in sys.argv:
        model = sys.argv[sys.argv.index("--model") + 1]

    if "--scenario" in sys.argv:
        num = int(sys.argv[sys.argv.index("--scenario") + 1])
        title, entry_agent, task_message = SCENARIOS[num]
    else:
        title, entry_agent, task_message = SCENARIOS[1]

    # -- Create actors ------------------------------------------------
    analyst = Actor(
        "analyst",
        prompt=(
            "You are a Market Analyst on an algorithmic trading desk.\n"
            "Your job:\n"
            "1. Analyze markets using fetch_market_data and run_technical_analysis\n"
            "2. Generate trade signals using generate_trade_signal for strong opportunities\n"
            "3. When you find a strong signal (BUY or SELL), use discover_agents to find\n"
            "   agents with 'execution' capability, then delegate_to the trader with\n"
            "   clear instructions on what to execute\n"
            "4. Summarize your analysis and any delegated trades\n\n"
            "Be decisive — if the signal is strong, delegate for execution."
        ),
        config=GeminiConfig(model=model, temperature=0.3),
        tools=analyst_tools,
    )

    trader = Actor(
        "trader",
        prompt=(
            "You are a Trader on an algorithmic trading desk.\n"
            "Your job:\n"
            "1. Receive trade signals and check current positions with check_position\n"
            "2. Place orders using place_order when instructed\n"
            "3. Calculate P&L with calculate_pnl as needed\n"
            "4. If a trade is large or risky, use discover_agents to find agents with\n"
            "   'risk' capability and delegate to the risk-manager for approval\n"
            "5. Report execution results clearly\n\n"
            "Always check position before trading. Report fills accurately."
        ),
        config=GeminiConfig(model=model, temperature=0.3),
        tools=trader_tools,
    )

    risk_manager = Actor(
        "risk-manager",
        prompt=(
            "You are the Risk Manager on an algorithmic trading desk.\n"
            "Your job:\n"
            "1. Monitor portfolio risk using calculate_var\n"
            "2. Check sector exposure using check_exposure\n"
            "3. Set risk limits using set_risk_limit when needed\n"
            "4. If you find compliance concerns, use discover_agents to find agents\n"
            "   with 'regulatory' or 'audit' capability and delegate to the\n"
            "   compliance-officer\n"
            "5. Provide clear risk assessments and recommendations\n\n"
            "Be conservative — protect the firm from excessive risk."
        ),
        config=GeminiConfig(model=model, temperature=0.3),
        tools=risk_tools,
    )

    compliance_officer = Actor(
        "compliance-officer",
        prompt=(
            "You are the Compliance Officer on an algorithmic trading desk.\n"
            "Your job:\n"
            "1. Check trades against regulatory rules using check_trade_rules\n"
            "2. Generate audit reports using generate_audit_report\n"
            "3. Flag violations using flag_violation\n"
            "4. When you need risk data, use discover_agents to find agents with\n"
            "   'risk' or 'exposure' capability and delegate to the risk-manager\n"
            "5. Ensure all activities are properly documented\n\n"
            "Be thorough — regulatory compliance is non-negotiable."
        ),
        config=GeminiConfig(model=model, temperature=0.3),
        tools=compliance_tools,
    )

    # -- Create network with full customization -----------------------
    risk_gate = RiskGate(max_exposure=1_000_000)
    compliance_audit = ComplianceAudit()
    telemetry = TelemetryPlugin()

    network = Network(
        topology=Pipeline(risk_gate, RateLimiter(max_per_minute=30)),
        plugins=[compliance_audit, telemetry],
        priority_scheme=TradePriorityScheme(),
        conflict_resolver=HighestPriorityWins(),
        max_delegation_depth=5,
    )

    # -- Register actors ----------------------------------------------
    await network.register(
        analyst,
        capabilities=["analysis", "research", "signals"],
        description="Market analyst - researches securities and generates trade signals",
    )
    await network.register(
        trader,
        capabilities=["execution", "orders", "positions"],
        description="Trader - executes orders and manages positions",
    )
    await network.register(
        risk_manager,
        capabilities=["risk", "exposure", "limits"],
        description="Risk manager - monitors portfolio risk and sets limits",
    )
    await network.register(
        compliance_officer,
        capabilities=["regulatory", "audit", "reporting"],
        description="Compliance officer - ensures regulatory compliance",
    )

    # -- Subscribe to hub stream for live logging ---------------------
    async def _on_hub_event(event: object) -> None:
        ts = datetime.now().strftime("%H:%M:%S.%f")[:-3]
        if isinstance(event, DelegationRequest):
            print(
                f"  {_DIM}{ts}{_RESET}  "
                f"{_MAGENTA}{_BOLD}ROUTE{_RESET}  "
                f"{_MAGENTA}{event.source.upper()} -> {event.target.upper()}{_RESET}"
            )
            preview = event.task[:120].replace("\n", " ")
            print(f"  {_DIM}{' ' * 12}{_RESET}         {_DIM}{preview}{'...' if len(event.task) > 120 else ''}{_RESET}")
        elif isinstance(event, DelegationResult):
            print(
                f"  {_DIM}{ts}{_RESET}  "
                f"{_GREEN}{_BOLD}DONE {_RESET}  "
                f"{_GREEN}{event.target.upper()} completed -> {event.source.upper()}{_RESET}"
            )
        elif isinstance(event, DelegationRejected):
            print(
                f"  {_DIM}{ts}{_RESET}  "
                f"{_RED}{_BOLD}VETO {_RESET}  "
                f"{_RED}{event.source.upper()} -> {event.target.upper()}: {event.reason}{_RESET}"
            )
        elif isinstance(event, ObserverAlert):
            print(
                f"  {_DIM}{ts}{_RESET}  "
                f"{_YELLOW}{_BOLD}ALERT{_RESET}  "
                f"{_YELLOW}[{event.severity.value}] {event.message}{_RESET}"
            )

    network.hub.stream.subscribe(_on_hub_event)

    # -- Print header -------------------------------------------------
    print()
    print(f"  {_BOLD}{'=' * 64}{_RESET}")
    print(f"  {_BOLD}ALGORITHMIC TRADING DESK{_RESET}  {_DIM}(06_trading_desk){_RESET}")
    print(f"  {_BOLD}{'=' * 64}{_RESET}")
    print()
    print(f"  {_CYAN}Scenario:{_RESET}   {title}")
    print(f"  {_CYAN}Model:{_RESET}      {model}")
    print(f"  {_CYAN}Entry:{_RESET}      {entry_agent}")
    print(f"  {_CYAN}Actors:{_RESET}     analyst, trader, risk-manager, compliance-officer")
    print()
    print(f"  {_BLUE}Customization points:{_RESET}")
    print("    Priority:   TradePriorityScheme (ANALYSIS < LIMIT < MARKET < RISK_ALERT)")
    print("    Events:     PriceSignal, TradeExecution (custom @register_event)")
    print("    Topology:   Pipeline(RiskGate, RateLimiter)")
    print("    Plugins:    ComplianceAudit (system), TelemetryPlugin (system)")
    print("    Conflict:   HighestPriorityWins")
    print()
    print(f"  {_BOLD}Task:{_RESET}")
    words = task_message.split()
    line = "    "
    for w in words:
        if len(line) + len(w) + 1 > 76:
            print(line)
            line = "    " + w
        else:
            line += " " + w if len(line) > 4 else w
    if len(line) > 4:
        print(line)
    print()
    print(f"  {_DIM}{'─' * 64}{_RESET}")
    print(
        f"  {_BOLD}Log legend:{_RESET}  "
        f"{_MAGENTA}ROUTE{_RESET}=delegation  "
        f"{_GREEN}DONE{_RESET}=completed  "
        f"{_RED}VETO{_RESET}=rejected  "
        f"{_YELLOW}ALERT{_RESET}=signal"
    )
    print(f"  {_DIM}{'─' * 64}{_RESET}")
    print()

    # -- Run ----------------------------------------------------------
    t0 = time.monotonic()
    reply = await network.ask(entry_agent, task_message)
    elapsed = time.monotonic() - t0

    # -- Print result -------------------------------------------------
    print()
    print(f"  {_BOLD}{'=' * 64}{_RESET}")
    print(f"  {_GREEN}{_BOLD}TRADING DESK RESPONSE{_RESET}  {_DIM}({elapsed:.1f}s){_RESET}")
    print(f"  {_BOLD}{'=' * 64}{_RESET}")
    print()
    for rline in (reply.body or "").split("\n"):
        print(f"  {rline}")
    print()

    # -- Print plugin metrics -----------------------------------------
    print(f"  {_BOLD}{'─' * 64}{_RESET}")
    print(f"  {_BLUE}{_BOLD}PLUGIN METRICS{_RESET}")
    print(f"  {_BOLD}{'─' * 64}{_RESET}")
    print()

    # Risk Gate
    print(f"  {_CYAN}RiskGate (topology/routing):{_RESET}")
    print(f"    Delegations checked: {risk_gate.total_checked}")
    print(f"    Delegations vetoed:  {risk_gate.total_vetoed}")
    print()

    # Telemetry
    print(f"  {_CYAN}TelemetryPlugin (system/observer):{_RESET}")
    print(f"    Total delegations:   {telemetry.metrics.total_delegations}")
    print(f"    Total completions:   {telemetry.metrics.total_completions}")
    if telemetry.metrics.by_source:
        print(f"    By source:           {dict(telemetry.metrics.by_source)}")
    if telemetry.metrics.by_target:
        print(f"    By target:           {dict(telemetry.metrics.by_target)}")
    print()

    # Compliance Audit
    audit_log = compliance_audit.get_audit_log()
    print(f"  {_CYAN}ComplianceAudit (system/observer):{_RESET}")
    print(f"    Audit entries:       {len(audit_log)}")
    print()
    if audit_log:
        print(f"  {_BOLD}{'─' * 64}{_RESET}")
        print(f"  {_BLUE}{_BOLD}COMPLIANCE AUDIT LOG{_RESET}")
        print(f"  {_BOLD}{'─' * 64}{_RESET}")
        print()
        for entry in audit_log:
            etype = entry["type"]
            if etype == "DELEGATION_REQUEST":
                color = _MAGENTA
                detail = f"{entry['source']} -> {entry['target']}: {entry['task_preview']}"
            elif etype == "DELEGATION_RESULT":
                color = _GREEN
                detail = f"{entry['source']} -> {entry['target']}: {entry['result_preview']}"
            elif etype == "DELEGATION_REJECTED":
                color = _RED
                detail = f"{entry['source']} -> {entry['target']}: {entry['reason']}"
            else:
                color = _WHITE
                detail = str(entry)
            print(f"    {_DIM}{entry['time']}{_RESET}  {color}{etype}{_RESET}")
            # Word-wrap the detail
            if len(detail) > 70:
                detail = detail[:70] + "..."
            print(f"    {' ' * 12}  {_DIM}{detail}{_RESET}")
        print()

    print(f"  {_DIM}{'─' * 64}{_RESET}")
    print(f"  {_DIM}Priority scheme: TradePriorityScheme | Conflict: HighestPriorityWins{_RESET}")
    print()

    await network.hub.close()


