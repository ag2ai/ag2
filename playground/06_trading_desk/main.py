@tool
async def calculate_var(portfolio: str, confidence_level: float = 0.95) -> str:
    """Calculate Value at Risk for a portfolio.

    Args:
        portfolio: Portfolio name or description.
        confidence_level: Confidence level (e.g. 0.95 or 0.99).
    """
    var_1d = round(random.uniform(50000, 500000), 2)
    var_10d = round(var_1d * 3.16, 2)  # sqrt(10) scaling
    return (
        f"VaR Calculation ({portfolio}):\n"
        f"  Confidence: {confidence_level:.0%}\n"
        f"  1-Day VaR: ${var_1d:,.2f}\n"
        f"  10-Day VaR: ${var_10d:,.2f}\n"
        f"  Method: Historical simulation\n"
        f"  Observation period: 252 trading days"
    )


@tool
async def check_exposure(sector: str) -> str:
    """Check portfolio exposure to a sector.

    Args:
        sector: Sector name (e.g. "technology", "energy", "healthcare").
    """
    exposure_pct = round(random.uniform(5, 45), 1)
    limit_pct = 40.0
    notional = round(random.uniform(500000, 5000000), 2)
    status = "WITHIN LIMITS" if exposure_pct < limit_pct else "LIMIT BREACH"
