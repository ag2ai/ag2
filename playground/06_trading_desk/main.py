class RiskGate(BasePlugin):
    """Routing plugin -- sits in the topology pipeline.

    Checks delegations that involve trading. If portfolio exposure exceeds
    the maximum, the delegation is vetoed (returns None).
    """

    def __init__(self, max_exposure: float = 1_000_000) -> None:
        self._max_exposure = max_exposure
        self._hub: Hub | None = None
        self.total_checked = 0
        self.total_vetoed = 0

    def install(self, hub: Hub) -> None:
        self._hub = hub

    def uninstall(self) -> None:
        self._hub = None

    async def process(self, envelope: Envelope, ctx: HubContext) -> Envelope | None:
        self.total_checked += 1
        event = envelope.event

        # Only gate trade-related delegations
        task_text = ""
        if isinstance(event, DelegationRequest):
            task_text = event.task.lower()

        trade_keywords = ("execute", "trade", "order", "buy", "sell", "place")
        is_trade = any(kw in task_text for kw in trade_keywords)

        if is_trade and self._hub is not None:
            exposure = await ctx.hub.state_store.get("portfolio_exposure")
            if exposure is not None and exposure > self._max_exposure:
                self.total_vetoed += 1
                return None  # Veto the delegation

        return envelope


class ComplianceAudit(BasePlugin):
    """System plugin -- observes hub.stream, does NOT sit in delegation path.

    Subscribes to DelegationRequest and DelegationResult events and
    records them in an in-memory audit trail.
    """

    def __init__(self) -> None:
        self._hub: Hub | None = None
        self._sub_ids: list = []
        self._audit_log: list[dict[str, Any]] = []

    def install(self, hub: Hub) -> None:
        self._hub = hub
        self._sub_ids.append(
            hub.stream.subscribe(
                self._on_request,
                condition=TypeCondition(DelegationRequest),
            )
        )
        self._sub_ids.append(
            hub.stream.subscribe(
                self._on_result,
                condition=TypeCondition(DelegationResult),
            )
        )
        self._sub_ids.append(
            hub.stream.subscribe(
                self._on_rejected,
                condition=TypeCondition(DelegationRejected),
            )
        )

    def uninstall(self) -> None:
        if self._hub:
            for sub_id in self._sub_ids:
                self._hub.stream.unsubscribe(sub_id)
        self._sub_ids.clear()
        self._hub = None

    async def _on_request(self, event: DelegationRequest) -> None:
        self._audit_log.append({
            "time": datetime.now().strftime("%H:%M:%S.%f")[:-3],
            "type": "DELEGATION_REQUEST",
            "source": event.source,
            "target": event.target,
            "task_preview": event.task[:100],
        })

    async def _on_result(self, event: DelegationResult) -> None:
        self._audit_log.append({
            "time": datetime.now().strftime("%H:%M:%S.%f")[:-3],
            "type": "DELEGATION_RESULT",
            "source": event.source,
            "target": event.target,
            "result_preview": event.result[:100],
        })

    async def _on_rejected(self, event: DelegationRejected) -> None:
        self._audit_log.append({
            "time": datetime.now().strftime("%H:%M:%S.%f")[:-3],
            "type": "DELEGATION_REJECTED",
            "source": event.source,
            "target": event.target,
            "reason": event.reason,
        })

    def get_audit_log(self) -> list[dict[str, Any]]:
        """Return the full audit trail."""
        return list(self._audit_log)


